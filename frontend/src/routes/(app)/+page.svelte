    <h2>Workflow Details</h2>

    <h3>1. Organisation</h3>
    <p>This component defines the operational scope of the GRC activities. It includes:</p>
    <ul>
        <li><strong>Assets</strong>: All tangible and intangible items of value to the organization.</li>
        <li><strong>Domains</strong>: Logical groupings of assets, such as departments or business units.</li>
        <li><strong>Perimeters</strong>: Specific boundaries for assessment, such as individual projects, products, or systems.</li>
    </ul>

    <h3>2. Library</h3>
    <p>A centralized repository of reference materials that inform the GRC process. It contains:</p>
    <ul>
        <li><strong>Catalog of Threats</strong>: A collection of potential threats that could impact organizational assets.</li>
        <li><strong>Frameworks</strong>: Industry standards and best practices (e.g., NIST, ISO 27001) used for compliance and risk management.</li>
        <li><strong>Reference Controls</strong>: A set of predefined controls that can be applied to mitigate risks.</li>
        <li><strong>Matrix</strong>: A tool for mapping relationships between different elements, such as threats and controls.</li>
    </ul>

    <h3>3. Risk</h3>
    <p>This section focuses on the systematic assessment and management of risks. The key steps include:</p>
    <ul>
        <li><strong>Risk Assessment</strong>: Scenarios are developed based on threats from the library to evaluate their potential impact on assets.</li>
        <li><strong>Current Risk Level</strong>: The level of risk before any new controls are applied.</li>
        <li><strong>Residual Risk Level</strong>: The level of risk remaining after controls have been implemented.</li>
    </ul>

    <h3>4. Compliance</h3>
    <p>This area ensures that the organization adheres to relevant frameworks and requirements. It involves:</p>
    <ul>
        <li><strong>Audit</strong>: Projects and systems are assessed against the requirements of one or more frameworks.</li>
        <li><strong>Compliance Assessment</strong>: The compliance of each requirement is evaluated to identify any gaps.</li>
    </ul>

    <h3>5. Action Plan</h3>
    <p>This is where risk mitigation and compliance efforts are put into action. It includes:</p>
    <ul>
        <li><strong>Applied Controls</strong>: Technical, policy-based, or procedural controls are implemented to address identified risks and compliance gaps.</li>
        <li><strong>Evidence</strong>: Documentation or other proof is collected to confirm that controls have been effectively implemented.</li>
    </ul>
